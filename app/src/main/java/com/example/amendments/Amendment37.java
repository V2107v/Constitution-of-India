package com.example.amendments;

import androidx.appcompat.app.AppCompatActivity;

import android.os.Bundle;
import android.widget.TextView;

import com.example.constitutionofindia.R;

public class Amendment37 extends AppCompatActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_amendment37);

        setTitle("Amendment 37");

        TextView textView = findViewById(R.id.content);

        textView.setText("THE CONSTITUTION (THIRTY-SEVENTH AMENDMENT) ACT, 1975\n" +
                "\n" +
                "Statement of Objects and Reasons appended to the Constitution\n" +
                "(Thirty-seventh Amendment) Bill, 1975 which was enacted as\n" +
                "the Constitution (Thirty-seventh Amendment) Act, 1975\n" +
                "\n" +
                "STATEMENT OF OBJECTS AND REASONS\n" +
                "\n" +
                "The Union territory of Arunachal Pradesh is at present administered\n" +
                "through a Chief Commissioner. It has a Pradesh Council, consisting\n" +
                "mostly of representatives of Zila Parishads, which functions as an\n" +
                "advisory body on important matters relating to the administration of\n" +
                "the Union territory. Some members of the Pradesh Council are also\n" +
                "associated with the Chief Commissioner in the day-to-day\n" +
                "administration as Counsellors. It is proposed to replace the Pradesh\n" +
                "Council by a Legislative Assembly and the Counsellors by a Council of\n" +
                "Ministers, as in certain other Union territories specified in article\n" +
                "239A of the Constitution. The Bill, therefore, seeks to include\n" +
                "Arunachal Pradesh in that article.\n" +
                "\n" +
                "2. Under article 240 of the Constitution, the President is empowered\n" +
                "to make regulations for the Union territory of Arunchal Pradesh. With\n" +
                "the Constitution of a Legislative Assembly for the Union territory, it\n" +
                "is proposed to provide that, as in the case of other Union territories\n" +
                "with Legislatures, this power may be exercised only when the Assembly\n" +
                "is either dissolved or its functioning remains suspended. The Bill\n" +
                "seeks to amend article 240 also to achieve this object.\n" +
                "\n" +
                "NEW DELHI; K. BRAHMANANDA REDDY.\n" +
                "\n" +
                "The 19th March, 1975.\n" +
                "\n" +
                "THE CONSTITUTION (THIRTY-SEVENTH AMENDMENT) ACT, 1975\n" +
                "\n" +
                "[3rd May, 1975.]\n" +
                "\n" +
                "An Act further to amend the Constitution of India.\n" +
                "\n" +
                "BE it enacted by Parliament in the Twenty-sixth Year of the Republic\n" +
                "of India as follows:-\n" +
                "\n" +
                "1. Short title.-This Act may be called the Constitution\n" +
                "(Thirty-seventh Amendment) Act, 1975.\n" +
                "\n" +
                "2. Amendment of article 239A.-In article 239A of the Constitution, in\n" +
                "clause (1), for the words \"Pondicherry and Mizoram\", the words\n" +
                "\"Pondicherry, Mizoram and Arunachal Pradesh\" shall be substituted.\n" +
                "\n" +
                "3. Amendment of article 240.-In article 240 of the Constitution, in\n" +
                "clause (1), in both the provisos, for the words \"Pondicherry or\n" +
                "Mizoram\", the words \"Pondicherry, Mizoram or Arunachal Pradesh\" shall\n" +
                "be substituted.");
    }
}
