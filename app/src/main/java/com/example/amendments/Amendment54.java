package com.example.amendments;

import androidx.appcompat.app.AppCompatActivity;

import android.os.Bundle;
import android.widget.TextView;

import com.example.constitutionofindia.R;

public class Amendment54 extends AppCompatActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_amendment54);

        setTitle("Amendment 54");

        TextView textView = findViewById(R.id.content);

        textView.setText("THE CONSTITUTION (FIFTY-FOURTH AMENDMENT) ACT, 1986\n" +
                "\n" +
                "Statement of Objects and Reasons appended to the Constitution\n" +
                "(Fifty-fourth Amendment) Bill, 1986 which was enacted as\n" +
                "the Constitution (Fifty-fourth Amendment) Act, 1986\n" +
                "\n" +
                "STATEMENT OF OBJECTS AND REASONS\n" +
                "\n" +
                "The salaries of Judges of the Supreme Court and the High Courts are\n" +
                "governed by the provisions of Part D of the Second Schedule to the\n" +
                "Constitution of India as indicated below:\n" +
                "\n" +
                "Chief Justice of India Rs. 5,000 per month\n" +
                "Judges of the Supreme Court Rs. 4,000 per month\n" +
                "Chief Justice of a High Court Rs. 4,000 per month\n" +
                "Judges of a High Court Rs. 3,500 per month\n" +
                "\n" +
                "2. These salaries have remained static since 1950, despite high\n" +
                "inflation and price rise that has taken place during all these years.\n" +
                "The Joint Conference of Chief Justices, Chief Ministers and Law\n" +
                "Ministers of the States held on 31st August and 1st September, 1985,\n" +
                "inter alia, discussed and recommended improvement in service\n" +
                "conditions of Judges including increase in their salaries, not only to\n" +
                "minimise the inflationary pressures on them but also to attract best\n" +
                "talents in the country to man the judicial posts.\n" +
                "\n" +
                "3. Having considered all aspects of the matter, it is proposed to\n" +
                "increase the salaries of the Judges as detailed below:\n" +
                "\n" +
                "Chief Justice of India Rs. 10,000 per month\n" +
                "Judges of the Supreme Court Rs. 9,000 per month\n" +
                "Chief Justice of a High Court Rs. 9,000 per month\n" +
                "Judges of a High Court Rs. 8,000 per month\n" +
                "\n" +
                "4. This Bill seeks to amend Part D of the Second Schedule to the\n" +
                "Constitution to give effect to the above increases in the salaries of\n" +
                "Judges and to make an enabling provision in articles 125 and 221 to\n" +
                "provide for changes in the salaries of Judges in future by Parliament\n" +
                "by law.\n" +
                "\n" +
                "NEW DELHI; A. K. SEN.\n" +
                "\n" +
                "The 4th August, 1986.\n" +
                "\n" +
                "THE CONSTITUTION (FIFTY-FOURTH AMENDMENT)\n" +
                "ACT, 1986\n" +
                "\n" +
                "[14th March, 1987.]\n" +
                "\n" +
                "An Act further to amend the Constitution of India.\n" +
                "\n" +
                "BE it enacted by Parliament in the Thirty-seventh Year of the Republic\n" +
                "of India as follows:-\n" +
                "\n" +
                "1. Short title and commencement.-(1) This Act may be called the\n" +
                "Constitution (Fifty-fourth Amendment) Act, 1986.\n" +
                "\n" +
                "(2) It shall be deemed to have come into force on the 1st day of\n" +
                "April, 1986.\n" +
                "\n" +
                "2. Amendment of article 125.-In article 125 of the Constitution, for\n" +
                "clause (1), the following clause shall be substituted, namely:-\n" +
                "\n" +
                "\"(1) There shall be paid to the Judges of the Supreme Court such\n" +
                "salaries as may be determined by Parliament by such law and, until\n" +
                "provision in that behalf is so made, salaries as are specified in the\n" +
                "Second Schedule.\".\n" +
                "\n" +
                "3. Amendment of article 221.-In article 221 of the Constitution, for\n" +
                "clause (1), the following clause shall be substituted, namely:-\n" +
                "\n" +
                "\"(1) There shall be paid to the Judges of each High Court such\n" +
                "salaries as may be determined by Parliament by such law and, until\n" +
                "provision in that behalf is so made, salaries as are specified in the\n" +
                "Second Schedule.\".\n" +
                "\n" +
                "4. Amendment of Second Schedule.-In the Second Schedule to the\n" +
                "Constitution, in Part D,-\n" +
                "\n" +
                "(a) in sub-paragraph (1) of paragraph 9,-\n" +
                "\n" +
                "(i) for the figures and word \"5,000 rupees\", the figures and word\n" +
                "\"10,000 rupees\" shall be substituted;\n" +
                "\n" +
                "(ii) for the figures and word \"4,000 rupees\", the figures and word\n" +
                "\"9,000 rupees\" shall be substituted;\n" +
                "\n" +
                "(b) in sub-paragraph (1) of paragraph 10,-\n" +
                "\n" +
                "(i) for the figures and word \"4,000 rupees\", the figures and word\n" +
                "\"9,000 rupees\" shall be substituted;\n" +
                "\n" +
                "(ii) for the figures and word \"3,500 rupees\", the figures and word\n" +
                "\"8,000 rupees\" shall be substituted.");
    }
}
