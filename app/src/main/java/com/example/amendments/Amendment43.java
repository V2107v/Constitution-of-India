package com.example.amendments;

import androidx.appcompat.app.AppCompatActivity;

import android.os.Bundle;
import android.widget.TextView;

import com.example.constitutionofindia.R;

public class Amendment43 extends AppCompatActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_amendment43);

        setTitle("Amendment 43");

        TextView textView = findViewById(R.id.content);

        textView.setText("THE CONSTITUTION (FORTY-THIRD AMENDMENT)\n" +
                "\n" +
                "Statement of Objects and Reasons appended to the Constitution\n" +
                "(Forty-fourth Amendment) Bill, 1977 (Bill No. 148 of 1977) which was\n" +
                "enacted as THE CONSTITUTION (Forty-third Amendment) Act, 1977\n" +
                "\n" +
                "STATEMENT OF OBJECTS AND REASONS\n" +
                "\n" +
                "The Constitution (Forty-second Amendment) Act, 1976, inserted various\n" +
                "articles in the Constitution to curtail, both directly and indirectly,\n" +
                "the jurisdiction of the Supreme Court and the High Courts to review\n" +
                "the constitutionality of laws. Article 32A barred the Supreme Court\n" +
                "from considering the constitutional validity of any State law in\n" +
                "proceedings for the enforcement of fundamental rights unless the\n" +
                "constitutional validity of any Central law was also in issue in such\n" +
                "proceedings. Article 131A gave to the Supreme Court exclusive\n" +
                "jurisdiction to decide the constitutional validity of a Central law\n" +
                "and thus deprived the High Courts of their jurisdiction in respect of\n" +
                "the same. Article 144A provided that the minimum number of Judges of\n" +
                "the Supreme Court who shall sit for the purpose of determining the\n" +
                "constitutional validity of any Central law or State law shall be seven\n" +
                "and required a special majority of two-thirds for the invalidation of\n" +
                "such law. Article 226A barred the High Courts from deciding the\n" +
                "validity of any Central law and article 228A required that there\n" +
                "should be a Bench of at least five Judges for determining the\n" +
                "constitutional validity of any State law and prescribed a special\n" +
                "majority for a judgment invalidating such a law.\n" +
                "\n" +
                "2. It is considered that articles 32A, 131A and 228A cause, hardship\n" +
                "to persons living in distant parts in India. Further, article 32A\n" +
                "would lead to multiplicity of proceedings as cases relating to the\n" +
                "validity of a State law which could be disposed of by the Supreme\n" +
                "Court itself have to be heard first by the High Court. The minimum\n" +
                "number of Judges in every case wherein the constitutional validity of\n" +
                "a law is involved, however unsubstantial the challenge might be,\n" +
                "results in valuable judicial time being lost in hearing and rejecting\n" +
                "submissions that have no substance. The Supreme Court has, in M/s.\n" +
                "Misrilal Jain vs. the State of Orissa and Others (AIR 1977 SC 1686)\n" +
                "expressed the hope that article 144A would engage the prompt attention\n" +
                "of Parliament and would be amended so as to leave to the court itself\n" +
                "the duty to decide how large a Bench should decide any particular\n" +
                "case. In fact, a number of cases have been held up in the Supreme\n" +
                "Court and High Courts as a result of the aforementioned articles.\n" +
                "\n" +
                "3. It is, therefore, proposed to omit articles 32A, 131A, 144A, 226A\n" +
                "and 228A. It is also proposed to make special provisions to enable\n" +
                "the Supreme Court and the High Courts to deal with pending cases in\n" +
                "the same manner as if the said articles had been omitted with effect\n" +
                "from the 1st February, 1977 (viz., the date on which those articles\n" +
                "were brought into force). Necessary consequential amendments to\n" +
                "articles 145, 228 and 366 are also proposed to be made.\n" +
                "\n" +
                "4. Article 31D confers special power on Parliament to enact certain\n" +
                "laws in respect of anti-national activities. It is considered that\n" +
                "these powers of Parliament to make laws for dealing with anti-national\n" +
                "activities and anti-national associations are of a sweeping nature and\n" +
                "are capable of abuse. It is, therefore, proposed to omit article 31D.\n" +
                "\n" +
                "5. The Bill seeks to achieve the above objects.\n" +
                "\n" +
                "NEW DELHI; SHANTI BHUSHAN.\n" +
                "\n" +
                "The 12th December, 1977.\n" +
                "\n" +
                "THE CONSTITUTION (FORTY-THIRD AMENDMENT)\n" +
                "ACT, 1977\n" +
                "\n" +
                "[13th April, 1978.]\n" +
                "\n" +
                "An Act further to amend the Constitution of India.\n" +
                "\n" +
                "BE it enacted by Parliament in the Twenty-eighth Year of the Republic\n" +
                "of India as follows:-\n" +
                "\n" +
                "1. Short title.-This Act may be called the Constitution (Forty-third\n" +
                "Amendment) Act, 1977.\n" +
                "\n" +
                "2. Omission of article 31D.- Article 31D of the Constitution shall be\n" +
                "omitted.\n" +
                "\n" +
                "3. Omission of article 32A.-(1) Article 32A of the Constitution shall\n" +
                "be omitted.\n" +
                "\n" +
                "(2) Any proceedings pending before the Supreme Court under article 32\n" +
                "of the Constitution immediately before the commencement of this Act\n" +
                "may be dealt with by the Supreme Court as if the said article 32A had\n" +
                "been omitted with effect on and from the 1st day of February, 1977.\n" +
                "\n" +
                "4. Omission of article 131A.-(1) Article 131A of the Constitution\n" +
                "shall be omitted.\n" +
                "\n" +
                "(2) Notwithstanding anything contained in sub-section (1), where\n" +
                "immediately before the commencement of this Act any reference made by\n" +
                "a High Court under the said article 131A is pending before the Supreme\n" +
                "Court, the Supreme Court may, having regard to-\n" +
                "\n" +
                "(a) the stage at which the reference is so pending; and\n" +
                "\n" +
                "(b) the ends of justice,\n" +
                "\n" +
                "either deal with the case as if that article had not been omitted or\n" +
                "return the case of the High Court for disposal as if that article had\n" +
                "been omitted with effect on and from the 1st day of February, 1977.\n" +
                "\n" +
                "5. Omission of article 144A.-(1) Article 144A of the Constitution\n" +
                "shall be omitted.\n" +
                "\n" +
                "(2) Any case pending before the Supreme Court immediately before the\n" +
                "commencement of this Act may be dealt with by the Supreme Court as if\n" +
                "the said article 144A had been omitted with effect on and from the 1st\n" +
                "day of February, 1977.\n" +
                "\n" +
                "6. Amendment of article 145.-In article 145 of the Constitution,-\n" +
                "\n" +
                "(a) in clause (1), in sub-clause (cc), for the words, figures and\n" +
                "letters \"articles 131A and 139A\", the word, figures and letter\n" +
                "\"article 139A\" shall be substituted;\n" +
                "\n" +
                "(b) in clause (2), the words, figures and letter \"article 144A and of\"\n" +
                "shall be omitted;\n" +
                "\n" +
                "(c) in clause (3), the words, figures and letter \"Subject to the\n" +
                "provisions of article 144A,\" shall be omitted.\n" +
                "\n" +
                "7. Amendment of article 226.-In article 226 of the Constitution, in\n" +
                "clause (1), the words, figures and letters \"but subject to the\n" +
                "provisions of article 131A and article 226A\" shall be omitted.\n" +
                "\n" +
                "8. Omission of article 226A.-(1) Article 226A of the Constitution\n" +
                "shall be omitted.\n" +
                "\n" +
                "(2) Any proceedings pending before a High Court under article 226 of\n" +
                "the Constitution immediately before the commencement of this Act may\n" +
                "be dealt with by the High Court as if the said article 226A had been\n" +
                "omitted with effect on and from the 1st day of February, 1977.\n" +
                "\n" +
                "9. Amendment of article 228.-In article 228 of the Constitution, in\n" +
                "the opening portion, the words, figures and letter \", subject to the\n" +
                "provisions of article 131A,\" shall be omitted.\n" +
                "\n" +
                "10. Omission of article 228A.-(1) Article 228A of the Constitution\n" +
                "shall be omitted.\n" +
                "\n" +
                "(2) Any case pending before a High Court immediately before the\n" +
                "commencement of this Act may be dealt with by the High Court as if the\n" +
                "said article 228A had been omitted with effect on and from the 1st day\n" +
                "of February, 1977.\n" +
                "\n" +
                "11. Amendment of article 366.-In article 366 of the Constitution,\n" +
                "clause (4A) and clause (26A) shall be omitted.");
    }
}
