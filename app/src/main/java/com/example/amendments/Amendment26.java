package com.example.amendments;

import androidx.appcompat.app.AppCompatActivity;

import android.os.Bundle;
import android.widget.TextView;

import com.example.constitutionofindia.R;

public class Amendment26 extends AppCompatActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_amendment26);

        setTitle("Amendment 26");

        TextView textView = findViewById(R.id.content);

        textView.setText("THE CONSTITUTION (TWENTY-SIXTH AMENDMENT) ACT, 1971\n" +
                "\n" +
                "Statement of Objects and Reasons appended to the Constitution\n" +
                "(Twenty-sixth Amendment) Bill, 1971 which was enacted as\n" +
                "THE CONSTITUTION (Twenty-sixth Amendment) Act, 1971\n" +
                "\n" +
                "STATEMENT OF OBJECTS AND REASONS\n" +
                "\n" +
                "The concept of rulership, with privy purses and special privileges\n" +
                "unrelated to any current functions and social purposes, is\n" +
                "incompatible with an egalitarian social order. Government have,\n" +
                "therefore, decided to terminate the privy purses and privileges of the\n" +
                "Rulers of former Indian States. It is necessary for this purpose,\n" +
                "apart from amending the relevant provisions of the Constitution, to\n" +
                "insert a new article therein so as to terminate expressly the\n" +
                "recognition already granted to such Rulers and to abolish privy purses\n" +
                "and extinguish all rights, liabilities and obligations in respect of\n" +
                "privy purses. Hence this Bill.\n" +
                "\n" +
                "NEW DELHI; INDIRA GANDHI.\n" +
                "\n" +
                "The 31st July, 1971.\n" +
                "\n" +
                "THE CONSTITUTION (TWENTY-SIXTH AMENDMENT) ACT, 1971\n" +
                "\n" +
                "[28th December, 1971.]\n" +
                "\n" +
                "An Act further to amend the Constitution of India.\n" +
                "\n" +
                "BE it enacted by Parliament in the Twenty-second Year of the Republic\n" +
                "of India as follows:---\n" +
                "\n" +
                "1. Short title.-This Act may be called the Constitution (Twenty-sixty\n" +
                "Amendment) Act, 1971.\n" +
                "\n" +
                "2. Omission of articles 291 and 362.-Articles 291 and 362 of the\n" +
                "Constitution shall be omitted.\n" +
                "\n" +
                "3. Insertion of new article 363A.-After article 363 of the\n" +
                "Constitution, the following article shall be inserted, namely:---\n" +
                "\n" +
                "\"363A. Recognition granted to Rulers of Indian States to cease and\n" +
                "privy purses to be abolished.- Notwithstanding anything in this\n" +
                "Constitution or in any law for the time being in force-\n" +
                "\n" +
                "(a) The Prince, Chief or other person who, at any time before the\n" +
                "commencement of THE CONSTITUTION (Twenty-sixth Amendment) Act, 1971,\n" +
                "was recognised by the President as the Ruler of an Indian State or any\n" +
                "person who, at any time before such commencement, was recognised by\n" +
                "the President as the successor of such Ruler shall, on and from such\n" +
                "commencement, cease to be recognised as such Ruler or the successor of\n" +
                "such Ruler;\n" +
                "\n" +
                "(b) on and from the commencement of the Constitution (Twenty-sixth\n" +
                "Amendment) Act, 1971, privy purse is abolished and all rights,\n" +
                "liabilities and obligations in respect of privy purse are extinguished\n" +
                "and accordingly the Ruler or, as the case may be, the successor of\n" +
                "such Ruler, referred to in clause (a) or any other person shall not be\n" +
                "paid any sum as privy purse.\".\n" +
                "\n" +
                "4. Amendment of article 366.-In article 366 of the Constitution, for\n" +
                "clause (22), the following clause shall be substituted, namely:-\n" +
                "\n" +
                "`(22) \"Ruler\" means the Prince, Chief or other person who, at any time\n" +
                "before the commencement of THE CONSTITUTION (Twenty-sixth Amendment)\n" +
                "Act, 1971, was recognised by the President as the Ruler of an Indian\n" +
                "State or any person who, at any time before such commencement, was\n" +
                "recognised by the President as the successor of such Ruler;'.");
    }
}
