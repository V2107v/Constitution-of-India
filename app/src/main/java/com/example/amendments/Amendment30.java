package com.example.amendments;

import androidx.appcompat.app.AppCompatActivity;

import android.os.Bundle;
import android.widget.TextView;

import com.example.constitutionofindia.R;

public class Amendment30 extends AppCompatActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_amendment30);

        setTitle("Amendment 30");

        TextView textView = findViewById(R.id.content);

        textView.setText("THE CONSTITUTION (THIRTIETH AMENDMENT) ACT, 1972\n" +
                "\n" +
                "Statement of Objects and Reasons appended to the Constitution\n" +
                "(Thirtieth Amendment) Bill, 1972 which was enacted as\n" +
                "the Constitution (Thirtieth Amendment) Act, 1973\n" +
                "\n" +
                "STATEMENT OF OBJECTS AND REASONS\n" +
                "\n" +
                "This Bill seeks to give effect to the recommendations of the Law\n" +
                "Commission of India in its Forty-fourth and Forty-fifth Reports on\n" +
                "Civil Appeals to the Supreme Court on certificate of fitness. At\n" +
                "present an appeal lies to the Supreme Court, inter alia, on a\n" +
                "certificate given by a High Court that the amount or the value of the\n" +
                "subject-matter of dispute is not less than twenty thousand rupees or\n" +
                "that the judgment, decree or final order involves, directly or\n" +
                "indirectly, some claim or question respecting property of the like\n" +
                "amount. The valuation cannot be the rational yardstick for a right to\n" +
                "appeal. An important question of law can arise even in suits of small\n" +
                "value and the test of valuation results in cases without merit going\n" +
                "up to the Supreme Court. The Law Commission recommended that clauses\n" +
                "(a) and (b) of article 133(1) of the Constitution should be omitted\n" +
                "and that an appeal should lie to the Supreme Court only if the High\n" +
                "Court certifies that the case involves a substantial question of law\n" +
                "of general importance and that in the opinion of the High Court the\n" +
                "said question needs to be decided by the Supreme Court. The amendment\n" +
                "of the article accordingly would curtail the number of appeals which\n" +
                "are filed in the Supreme Court merely on the valuation test being\n" +
                "satisfied, without any merit in them. The Bill seeks to achieve this\n" +
                "object.\n" +
                "\n" +
                "NEW DELHI; H. R. GOKHALE.\n" +
                "\n" +
                "The 8th May, 1972.\n" +
                "\n" +
                "THE CONSTITUTION (THIRTIETH AMENDMENT) ACT, 1972\n" +
                "\n" +
                "[22nd February, 1973.]\n" +
                "\n" +
                "An Act further to amend the Constitution of India.\n" +
                "\n" +
                "BE it enacted by Parliament the Twenty-third Year of the Republic of\n" +
                "India as follows:-\n" +
                "\n" +
                "1. Short title and commencement.-(1) This Act may be called the\n" +
                "Constitution (Thirtieth Amendment) Act, 1972.\n" +
                "\n" +
                "(2) It shall come into force on such date_665 as the Central\n" +
                "Government may, by notification in the Official Gazette, appoint.\n" +
                "\n" +
                "2. Amendment of article 133.-In article 133 of the Constitution, for\n" +
                "clause (1), the following clause shall be substituted, namely:-\n" +
                "\n" +
                "\"(1) An appeal shall lie to the Supreme Court from any judgment,\n" +
                "decree or final order in a civil proceeding of a High Court in the\n" +
                "territory of India if the High Court certifies-\n" +
                "\n" +
                "(a) that the case involves a substantial question of law of general\n" +
                "importance; and\n" +
                "\n" +
                "(b) that in the opinion of the High Court the said question needs to\n" +
                "be decided by the Supreme Court.\".\n" +
                "\n" +
                "3. Special provision as to pending proceedings, etc.-(1) Nothing in\n" +
                "this Act shall affect-\n" +
                "\n" +
                "(a) any appeal under sub-clause (a) or sub-clause (b) or sub-clause\n" +
                "(c) of clause (1) of article 133 of the Constitution which immediately\n" +
                "before the commencement of this Act was pending before the Supreme\n" +
                "Court; or\n" +
                "\n" +
                "(b) any appeal preferred on or after the commencement of this Act\n" +
                "against any judgement, decree or final order in a civil proceeding of\n" +
                "a High Court by virtue of a certificate given by the High Court before\n" +
                "the commencement of this Act under sub-clause (a) or sub-clause (b) or\n" +
                "sub-clause (c) of clause (1) of article 133.\n" +
                "\n" +
                "and every such appeal may be heard and disposed of or, as the case may\n" +
                "be, entertained, heard and disposed of by the Supreme Court as if this\n" +
                "Act had not been passed.\n" +
                "\n" +
                "(2) Subject to the provisions of sub-section (1), no appeal shall lie\n" +
                "to the Supreme Court under clause (1) of article 133 of the\n" +
                "Constitution from any judgment, decree or final order arising out of a\n" +
                "suit or other civil proceeding which was instituted or commenced in\n" +
                "any court before the commencement of this Act unless such appeal\n" +
                "satisfies the provisions of that clause as amended by this Act.");
    }
}
