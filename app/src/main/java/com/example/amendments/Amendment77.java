package com.example.amendments;

import androidx.appcompat.app.AppCompatActivity;

import android.os.Bundle;
import android.widget.TextView;

import com.example.constitutionofindia.R;

public class Amendment77 extends AppCompatActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_amendment77);

        setTitle("Amendment 77");

        TextView textView = findViewById(R.id.content);

        textView.setText("THE CONSTITUTION (SEVENTY-SEVENTH AMENDMENT) ACT, 1995\n" +
                "\n" +
                "Statement of Objects and Reasons appended to the Constitution\n" +
                "(Eighty-sixth Amendment) Bill, 1995 which was enacted as\n" +
                "THE CONSTITUTION (Seventy-seventh Amendment) Act, 1995\n" +
                "\n" +
                "STATEMENT OF OBJECTS AND REASONS\n" +
                "\n" +
                "The Scheduled Castes and the Scheduled Tribes have been enjoying the\n" +
                "facility of reservation in promotion since 1955. The Supreme Court in\n" +
                "its judgment dated 16th November, 1992 in the case of Indra Sawhney\n" +
                "and Others vs. Union of India and Others, however, observed that\n" +
                "reservation of appointments or posts under article 16(4) of the\n" +
                "Constitution is confined to initial appointment and cannot extent to\n" +
                "reservation in the matter of promotion. This ruling of the Supreme\n" +
                "Court will adversely affect the interests of the Scheduled Castes and\n" +
                "the Scheduled Tribes. Since the representation of the Scheduled\n" +
                "Castes and the Scheduled Tribes in services in the States have not\n" +
                "reached the required level, it is necessary to continue the existing\n" +
                "dispensation of providing reservation in promotion in the case of the\n" +
                "Scheduled Castes and the Scheduled Tribes. In view of the commitment\n" +
                "of the Government to protect the interest of the Scheduled Castes and\n" +
                "the Scheduled Tribes, the Government have decided to continue the\n" +
                "existing policy of reservation in promotion for the Scheduled Castes\n" +
                "and the Scheduled Tribes. To carry out this, it is necessary to amend\n" +
                "article 16 of the Constitution by inserting a new clause (4A) in the\n" +
                "said article to provide for reservation in promotion for the Scheduled\n" +
                "Castes and the Scheduled Tribes.\n" +
                "\n" +
                "2. The Bill seeks to achieve the aforesaid object.\n" +
                "\n" +
                "NEW DELHI; SITARAM KESRI.\n" +
                "\n" +
                "The 31st May, 1995.\n" +
                "\n" +
                "THE CONSTITUTION (SEVENTY-SEVENTH AMENDMENT)\n" +
                "ACT, 1995\n" +
                "\n" +
                "[17th June, 1995.]\n" +
                "\n" +
                "An Act further to amend the Constitution of India.\n" +
                "\n" +
                "BE it enacted by Parliament in the Forty-sixth Year of the Republic of\n" +
                "India as follows:-\n" +
                "\n" +
                "1. Short title.-This Act may be called the Constitution\n" +
                "(Seventy-seventh Amendment) Act, 1995.\n" +
                "\n" +
                "2. Amendment of article 16.-In article 16 of the Constitution, after\n" +
                "clause (4), the following clause shall be inserted, namely:-\n" +
                "\n" +
                "\"(4A) Nothing in this article shall prevent the State from making any\n" +
                "provision for reservation in matters of promotion to any class or\n" +
                "classes of posts in the services under the State in favour of the\n" +
                "Scheduled Castes and the Scheduled Tribes which, in the opinion of the\n" +
                "State, are not adequately represented in the services under the\n" +
                "State.\".");
    }
}
