package com.example.amendments;

import androidx.appcompat.app.AppCompatActivity;

import android.os.Bundle;
import android.widget.TextView;

import com.example.constitutionofindia.R;

public class Amendment25 extends AppCompatActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_amendment25);

        setTitle("Amendment 25");

        TextView textView = findViewById(R.id.content);

        textView.setText("THE CONSTITUTION (TWENTY-FIFTH AMENDMENT) ACT, 1971\n" +
                "\n" +
                "Statement of Objects and Reasons appended to the Constitution\n" +
                "(Twenty-fifth Amendment) Bill, 1971 which was enacted as the\n" +
                "Constitution (Twenty-fifth Amendment) Act, 1971\n" +
                "\n" +
                "STATEMENT OF OBJECTS AND REASONS\n" +
                "\n" +
                "Article 31 of the Constitution as it stands specifically provides that\n" +
                "no law providing for the compulsory acquisition or requisitioning of\n" +
                "property which either fixes the amount of compensation or specifies\n" +
                "the principles on which and the manner in which the compensation is to\n" +
                "be determined and given shall be called in question in any court on\n" +
                "the ground that the compensation provided by that law is not adequate.\n" +
                "In the Bank Nationalization case [1970, 3 S.C.R. 530), the Supreme\n" +
                "Court has held that the Constitution guarantees right to compensation,\n" +
                "that is, the equivalent in money of the property compulsorily\n" +
                "acquired. Thus in effect the adequacy of compensation and the\n" +
                "relevancy of the principles laid down by the Legislature for\n" +
                "determining the amount of compensation have virtuality become\n" +
                "justiciable inasmuch as the Court can go into the question whether the\n" +
                "amount paid to the owner of the property is what may be regarded\n" +
                "reasonably as compensation for loss of property. In the same case,\n" +
                "the Court has also held that a law which seeks to acquire or\n" +
                "requisition property for a public purpose should also satisfy the\n" +
                "requirements of article 19 (1) (f).\n" +
                "\n" +
                "2. The Bill seeks to surmount the difficulties placed in the way of\n" +
                "giving effect to the Directive Principles of State Policy by the\n" +
                "aforesaid interpretation. The word \"compensation\" is sought to be\n" +
                "omitted from article 31(2) and replaced by the word \"amount\". It is\n" +
                "being clarified that the said amount may be given otherwise than in\n" +
                "cash. It is also proposed to provide that article 19(1)(f) shall not\n" +
                "apply to any law relating to the acquisition or requisitioning of\n" +
                "property for a public purpose.\n" +
                "\n" +
                "3. The Bill further seeks to introduce a new article 31C which\n" +
                "provides that if any law is passed to give effect to the Directive\n" +
                "Principles contained in clauses (b) and (c) of article 39 and contains\n" +
                "a declaration to that effect, such law shall not be deemed to be void\n" +
                "on the ground that it takes away or abridges any of the rights\n" +
                "contained in article 14, 19 or 31 and shall not be questioned on the\n" +
                "ground that it does not give effect to those principles. For this\n" +
                "provision to apply in the case of laws made by State Legislatures, it\n" +
                "is necessary that the relevant Bill should be reserved for the\n" +
                "consideration of the President and receive his assent.\n" +
                "\n" +
                "NEW DELHI; H. R. GOKHALE.\n" +
                "\n" +
                "The 22nd July, 1971.\n" +
                "\n" +
                "THE CONSTITUTION (TWENTY-FIFTH AMENDMENT) ACT, 1971\n" +
                "\n" +
                "[20th April, 1972.]\n" +
                "\n" +
                "An Act further to amend the Constitution of India.\n" +
                "\n" +
                "BE it enacted by Parliament in the Twenty-second Year of the Republic\n" +
                "of India as follow:-\n" +
                "\n" +
                "1. Short title.-This Act may be called the Constitution (Twenty-fifth\n" +
                "Amendment) Act, 1971.\n" +
                "\n" +
                "2. Amendment of article 31.-In article 31 of the Constitution,-\n" +
                "\n" +
                "(a) for clause (2), the following clause shall be substituted,\n" +
                "namely:-\n" +
                "\n" +
                "\"(2) No property shall be compulsorily acquired or requisitioned save\n" +
                "for a public purpose and save by authority of a law which provides for\n" +
                "acquisition or requisitioning of the property for an amount which may\n" +
                "be fixed by such law or which may be determined in accordance with\n" +
                "such principles and given in such manner as may be specified in such\n" +
                "law; and no such law shall be called in question in any court on the\n" +
                "ground that the amount so fixed or determined is not adequate or that\n" +
                "the whole or any part of such amount is to be given otherwise than in\n" +
                "cash:\n" +
                "\n" +
                "Provided that in making any law providing for the compulsory\n" +
                "acquisition of any property of an educational institution established\n" +
                "and administered by a minority, referred to in clause (1) of article\n" +
                "30, the State shall ensure that the amount fixed by or determined\n" +
                "under such law for the acquisition of such property is such as would\n" +
                "not restrict or abrogate the right guaranteed under that clause.\";\n" +
                "\n" +
                "(b) after clause (2A), the following clause shall be inserted,\n" +
                "namely:-\n" +
                "\n" +
                "\"(2B) Nothing in sub-clause (f) of clause (1) of article 19 shall\n" +
                "affect any such law as is referred to in clause (2).\".\n" +
                "\n" +
                "3. Insertion of new article 31C.-After article 31B of the\n" +
                "Constitution, the following article shall be inserted, namely:-\n" +
                "\n" +
                "\"31C. Saving of laws giving effect to certain directive principles.-\n" +
                "Notwithstanding anything contained in article 13, no law giving effect\n" +
                "to the policy of the State towards securing the principles specified\n" +
                "in clause (b) or clause (c) of article 39 shall be deemed to be void\n" +
                "on the ground that it is inconsistent with, or takes away or abridges\n" +
                "any of the rights conferred by article 14, article 19 or article 31;\n" +
                "and no law containing a declaration that it is for giving effect to\n" +
                "such policy shall be called in question in any court on the ground\n" +
                "that it does not give effect to such policy:\n" +
                "\n" +
                "Provided that where such law is made by the Legislature of a State,\n" +
                "the provisions of this article shall not apply thereto unless such\n" +
                "law, having been reserved for the consideration of the President, has\n" +
                "received his assent.\".");
    }
}
