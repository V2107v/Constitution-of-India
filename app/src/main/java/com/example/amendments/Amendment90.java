package com.example.amendments;

import androidx.appcompat.app.AppCompatActivity;

import android.os.Bundle;
import android.widget.TextView;

import com.example.constitutionofindia.R;

public class Amendment90 extends AppCompatActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_amendment90);

        setTitle("Amendment 90");

        TextView textView = findViewById(R.id.content);

        textView.setText("THE CONSTITUTION (NINETIETH AMENDMENT) ACT, 2003\n" +
                "\n" +
                "[28th September, 2003.]\n" +
                "\n" +
                "An Act further to amend the Constitution of India.\n" +
                "\n" +
                "BE it enacted by Parliament in the Fifty-fourth Year of the Republic\n" +
                "of India as follows:-\n" +
                "\n" +
                "1. Short title - This Act may be called the Constitution (Ninetieth Amendment)\n" +
                "Act, 2003.\n" +
                "\n" +
                "2. Amendment of article 332. - In article 332 of the Constitution, in clause (6), the following proviso shall be inserted, namely:-\n" +
                "\n" +
                "\"Provided that for elections to the Legislative Assembly of the State of Assam,\n" +
                "the representation of the Scheduled Tribes and non-Scheduled Tribes in the\n" +
                "constituencies included in the Bodoland Territorial Areas District, so\n" +
                "notified, and existing prior to the constitution of the Bodoland Territorial\n" +
                "Areas District, shall be maintained.\".\n" +
                "\n" +
                "SUBHASH C. JAIN,\n" +
                "\n" +
                "Secy. to the Govt. of India.\n" +
                "\n" +
                "STATEMENT OF OBJECTS AND REASONS\n" +
                "\n" +
                "Article 332 of the Constitution of India provides for reservation of seats for the Scheduled Castes and the Scheduled Tribes in the Legislative Assemblies of the States. Clause (6) of article 332.stipulates that no person who is not a member of a Scheduled Tribe of any autonomous district of the State of Assam shall be eligible for election to the Legislative Assembly of the State from any constituency of that district.\n" +
                "\n" +
                "2. In pursuance of the Memorandum of Settlement signed on the 10th day of February, 2003 between the Government of India, Government of Assam and Bodo Liberation Tigers, and to protect the rights of the non-tribals, the existing representation of the Scheduled Tribes and non-Scheduled Tribes in the Legislative Assembly of the State of Assam from the Bodoland Territorial Council Areas District is proposed to be kept intact. It is, therefore, proposed to insert a proviso in clause (6) of article 332 of the Constitution.\n" +
                "\n" +
                "3. The Bill seeks to achieve the above objects.\n" +
                "\n" +
                "L. K. ADVANI.\n" +
                "\n" +
                "NEW DELHI;\n" +
                "\n" +
                "The 7th May, 2003");
    }
}
