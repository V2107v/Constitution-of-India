package com.example.amendments;

import androidx.appcompat.app.AppCompatActivity;

import android.os.Bundle;
import android.widget.TextView;

import com.example.constitutionofindia.R;

public class Amendment81 extends AppCompatActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_amendment81);

        setTitle("Amendment 81");

        TextView textView = findViewById(R.id.content);

        textView.setText("THE CONSTITUTION (EIGHTY FIRST AMENDMENT) ACT, 2000\n" +
                "\n" +
                "Statement of Objects and Reasons appended to the Constitution (Ninetieth Amendment) Bill, 2000 which was enacted as the Constitution (Eighty-first Amendment) Act, 2000.\n" +
                "\n" +
                "STATEMENT OF OBJECTS AND REASONS\n" +
                "\n" +
                "Prior to August, 29, 1997, the vacancies reserved for the Scheduled Castes and the Scheduled Tribes, which could not be filled up by direct recruitment on account of non-availability of the candidates belonging to the Scheduled Castes or the Scheduled Tribes, were treated as \"Backlog Vacancies\". These vacancies were treated as a distinct group and were excluded from the ceiling of fifty per cent reservation. The Supreme Court of India in its judgment in the Indra Sawhney versus Union of India held that the number of vacancies to be filled up on the basis of reservations in a year including carried forward reservations should in no case exceed the limit of fifty per cent. As total reservations in a year for the Scheduled Castes, the Scheduled Tribes and the other Backward Classes combined together had already reached forty-nine and a half per cent and the total number of vacancies to be filled up in a year could not exceed fifty per cent., it became difficult to fill the \"Backlog Vacancies\" and to hold Special Recruitment Drives. Therefore, to implement the judgment of the Supreme Court, an Official Memorandum dated August 29, 1997 was issued to provide that the fifty per cent limit shall apply to current as well as \"Backlog Vacancies\" and for discontinuation of the Special Recruitment Drive.\n" +
                "\n" +
                "2. Due to the adverse effect of the aforesaid order dated August 29, 1997, various organisations including the Members of Parliament represented to the central Government for protecting the interest of the Scheduled castes and the Scheduled Tribes. The Government, after considering various representations, reviewed the position and has decided to make amendment in the constitution so that the unfilled vacancies of a year, which are reserved for being filled up in that year in accordance with any provision for reservation made under clause (4) or clause (4A) of article 16 of the Constitution, shall be considered as a separate class of vacancies to be filled up in any succeeding year or years and such class of vacancies shall not be considered together with the vacancies of the year in which they are being filled up for determining the ceiling of fifty percent. Reservation on total number of vacancies of that year. This amendment in the Constitution would enable the State to restore the position as was prevalent before august 29, 1997.\n" +
                "\n" +
                "3. That Bill seeks to achieve the aforesaid object.\n" +
                "\n" +
                "NEW DLEHI; VASUNDHRA RAJE\n" +
                "\n" +
                "The 1st May, 2000.\n" +
                "\n" +
                "THE CONSTITUTION (EIGHTY-FIRST AMENDMENT) ACT, 2000\n" +
                "\n" +
                "[9th June, 2000]\n" +
                "\n" +
                "An Act further to amend the Constitution of India.\n" +
                "\n" +
                "BE it enacted by Parliament in the Fiftieth Year of the Republic of India as follows:-\n" +
                "\n" +
                "1. Short title: This Act may be called the Constitution (Eighty-first Amendment) Act, 2000.\n" +
                "\n" +
                "2. Amendment of article 16: In article 16 of the Constitution, for clause (4A), the following clauses shall be inserted, namely: -\n" +
                "\n" +
                "\"(4B) Nothing in this article shall prevent the State from considering any unfilled vacancies of a year which are reserved for being filled up in that year in accordance with any provision for reservation made under clause (4) or clause (4A) as a separate class of vacancies to be filled up in any succeeding year or years and such class of vacancies shall not be considered together with the vacancies of the year in which they are being filled up for determining the ceiling of fifty per cent reservation on total number of vacancies of that year\".");
    }
}
