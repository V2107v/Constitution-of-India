package com.example.amendments;

import androidx.appcompat.app.AppCompatActivity;

import android.os.Bundle;
import android.widget.TextView;

import com.example.constitutionofindia.R;

public class Amendment60 extends AppCompatActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_amendment60);

        setTitle("Amendment 60");

        TextView textView = findViewById(R.id.content);

        textView.setText("THE CONSTITUTION (SIXTIETH AMENDMENT) ACT, 1988\n" +
                "\n" +
                "Statement of Objects and Reasons appended to the Constitution\n" +
                "(Sixtieth Amendment) Bill, 1988 which was enacted as\n" +
                "THE CONSTITUTION (Sixtieth Amendment) Act, 1988\n" +
                "\n" +
                "STATEMENT OF OBJECTS AND REASONS\n" +
                "\n" +
                "Clause (2) of article 276 of the Constitution specifies that the total\n" +
                "amount payable in respect of any one person to the State or to any one\n" +
                "municipality, district board, local board or other local authority in\n" +
                "a State by way of taxes on professions, trades, callings and\n" +
                "employments leviable by a State Legislature under clause (1) of that\n" +
                "article shall not exceed two hundred and fifty rupees per annum. The\n" +
                "proviso to that clause, however, enables the continuance of the levy\n" +
                "of such tax at a rate exceeding two hundred and fifty rupees per annum\n" +
                "in any State, municipality, etc., if in the financial year immediately\n" +
                "preceding the commencement of Constitution there was in force in that\n" +
                "State, municipality, etc., any such tax exceeding that rate.\n" +
                "\n" +
                "2. Some of the State Governments have represented that this celling\n" +
                "of two hundred and fifty rupees which was fixed in 1949, needs to be\n" +
                "revised upwards taking into consideration the price rise and other\n" +
                "factors. It is also pointed out that the profession tax has, at\n" +
                "present, become almost regressive because of the ceiling since even\n" +
                "people with high salaries have to pay this tax at only the maximum\n" +
                "amount of two hundred and fifty rupees per annum. The upward revision\n" +
                "of profession tax will also help the State Governments in raising\n" +
                "additional resources.\n" +
                "\n" +
                "3. It is accordingly proposed to amend clause (2) of article 276 of\n" +
                "the Constitution to increase the ceiling of profession tax from two\n" +
                "hundred and fifty rupees per annum to two thousand and five hundred\n" +
                "rupees per annum. As the proviso to this clause is no longer\n" +
                "relevant, it is proposed to omit it.\n" +
                "\n" +
                "4. The Bill seeks to achieve the above objects.\n" +
                "\n" +
                "NEW DELHI; B.K. GADHVI.\n" +
                "\n" +
                "The 10th August, 1988.\n" +
                "\n" +
                "THE CONSTITUTION (SIXTIETH AMENDMENT) ACT, 1988\n" +
                "\n" +
                "[20th December, 1988.]\n" +
                "\n" +
                "An Act further to amend the Constitution of India.\n" +
                "\n" +
                "BE it enacted by Parliament in the Thirty-ninth Year of the Republic\n" +
                "of India as follows:-\n" +
                "\n" +
                "1. Short title.-This Act may be called the Constitution (Sixtieth\n" +
                "Amendment) Act, 1988.\n" +
                "\n" +
                "2. Amendment of article 276.-In article 276 of the Constitution, in\n" +
                "clause (2),-\n" +
                "\n" +
                "(a) for the words \"two hundred and fifty rupees\", the words \"two\n" +
                "thousand and five hundred rupees\" shall be substituted;\n" +
                "\n" +
                "(b) the proviso shall be omitted.");
    }
}
