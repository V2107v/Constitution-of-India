package com.example.amendments;

import androidx.appcompat.app.AppCompatActivity;

import android.os.Bundle;
import android.widget.TextView;

import com.example.constitutionofindia.R;

public class Amendment82 extends AppCompatActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_amendment82);

        setTitle("Amendment 82");

        TextView textView = findViewById(R.id.content);

        textView.setText("THE CONSTITUTION (EIGHTY SECOND AMENDMENT) ACT 2000\n" +
                "\n" +
                "Statement of Objects and Reasons appended to the Constitution (Eighty Eighth Amendment) Bill 1999 which was enacted as the Constitution (Eighty Second Amendment) Act 2000\n" +
                "\n" +
                "The Scheduled Castes and the Scheduled Tribes had been enjoying the facility of relaxation of qualifying marks and standards of evaluation in matters of reservation in promotion. The Supreme Court in its judgment dated 1-10-1996 in the case of S. Vinod Kumar Vs. Union India held that such relaxations in matters of reservation in promotion were not permissible under article 16(4) of the Constitution in view of the command contained in article 335 of the Constitution. The Apex Court also held that the law on the subject of relaxations of qualifying marks and standards of evaluation in matters of reservation in promotion is one laid down by the nine-judge Constitution Bench of the Supreme Court in the case of Indira Sawhney and others Vs. Union of India and others. Para 831 of Indira Sawhney judgment also held such relaxations as being not permissible under article 16(4) in view of the command contained in article 335 of the Constitution. In order to implement the judgments of the Supreme Court, such relaxations had to be withdrawn with effect from 22.07.1997.\n" +
                "\n" +
                "2. In view of the adverse effect of the order dated 22.07.1997 on the interests of Scheduled Castes and Scheduled Tribes, representations had been received by the Government from several quarters including the Members of Parliament. Considering the various representations, the Government has reviewed the position and decided to move for constitutional amendment with a view to restore the relaxations which were withdrawn vide instructions issued by the Department of Personnel and Training on 22.07.1997.\n" +
                "\n" +
                "VASUNDHARA RAJE\n" +
                "\n" +
                "New Delhi;\n" +
                "\n" +
                "The 22nd December, 1999\n" +
                "\n" +
                "THE CONSTITUTION (EIGHTY SECOND AMENDMENT) ACT, 2000\n" +
                "\n" +
                "[8th September 200]\n" +
                "\n" +
                "An Act further to amend the Constitution of India.\n" +
                "\n" +
                "BE it enacted by Parliament in the Fifty-first year of the Republic of India as follows :—\n" +
                "\n" +
                "1. Short title: This Act may be called the Constitution (Eighty-second Amendment) Act, 2000\n" +
                "\n" +
                "2. Amendment of article 335: In Article 335 of the Constitution, the following proviso shall be inserted at the end,\n" +
                "\n" +
                "namely :—\n" +
                "\n" +
                "\"Provided that nothing in this article shall prevent in making of any provision in favour of the members of the Scheduled Castes and the Scheduled Tribes for relaxation in qualifying marks in any examination or lowering the standards of evaluation, for reservation in matters of promotion to any class or classes of services or posts in connection with the affairs of the Union or of a State\".");
    }
}
