package com.example.amendments;

import androidx.appcompat.app.AppCompatActivity;

import android.os.Bundle;
import android.widget.TextView;

import com.example.constitutionofindia.R;

public class Amendment08 extends AppCompatActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_amendment08);

        setTitle("Amendment 08");

        TextView textView = findViewById(R.id.content);

        textView.setText("THE CONSTITUTION (EIGHTH AMENDMENT) ACT, 1959\n" +
                "\n" +
                "Statement of Objects and Reasons appended to THE CONSTITUTION (Fourth Amendment) Bill, 1954 which was enacted as the Constitution (Fourth Amendment) Act, 1954\n" +
                "\n" +
                "STATEMENT OF OBJECTS AND REASONS\n" +
                "\n" +
                "This  Bill  seeks to amend articles 31, 31A and 305 of, and the  Ninth Schedule to, the Constitution.\n" +
                "\n" +
                "2.   Recent  decisions  of the Supreme Court have given  a  very  wide\n" +
                "meaning  to clauses (1) and (2) of article 31.  Despite the difference\n" +
                "in  the wording of the two clauses, they are regarded as dealing  with\n" +
                "the  same subject.  The deprivation of property referred to in  clause\n" +
                "(1)  is  to  be  construed  in  the  widest  sense   as  including  any\n" +
                "curtailment  of  a  right to property.  Even where it is caused   by  a\n" +
                "purely  regulatory  provision  of  law and is not  accompanied   by  an\n" +
                "acquisition  or taking possession of that or any other property  right\n" +
                "by  the  State,  the  law, in order to be  valid  according   to  these\n" +
                "decisions,  has  to provide for compensation under clause (2)  of  the\n" +
                "article.   It  is  considered necessary, therefore, to  re-state   more\n" +
                "precisely   the   State's   power  of    compulsory   acquisition   and\n" +
                "requisitioning of private property and distinguish it from cases where\n" +
                "the  operation of regulatory or prohibitory laws of the State  results\n" +
                "in  \"deprivation of property\".  This is sought to be done in clause   2\n" +
                "of the Bill.\n" +
                "\n" +
                "3.   It will be recalled that the zamindari abolition laws which  came\n" +
                "first  in our programme of social welfare legislation were attacked by\n" +
                "the  interests  affected mainly with reference to articles 14, 19  and\n" +
                "31,  and  that  in order to put an end to the  dilatory  and   wasteful\n" +
                "litigation  and  place  these  laws above  challenge  in   the  courts,\n" +
                "articles  31A  and  31B  and the Ninth Schedule were  enacted   by  the\n" +
                "Constitution  (First  Amendment) Act.  Subsequent  judicial   decisions\n" +
                "interpreting  articles 14, 19 and 31 have raised serious  difficulties\n" +
                "in  the  way  of the Union and the States putting  through  other   and\n" +
                "equally  important  social welfare legislation on the  desired  lines,\n" +
                "e.g., the following:-\n" +
                "\n" +
                "(i)  While the abolition of zamindaris and the numerous intermediaries\n" +
                "between the State and the tiller of the soil has been achieved for the\n" +
                "most part, our next objectives in land reform are the fixing of limits\n" +
                "to  the  extent of agricultural land that may be owned or occupied  by\n" +
                "any  person, the disposal of any land held in excess of the prescribed\n" +
                "maximum  and the further modification of the rights of land owners and\n" +
                "tenants in agricultural holdings.\n" +
                "\n" +
                "(ii)  The  proper  planning  of   urban and  rural  areas  require  the\n" +
                "beneficial  utilisation of vacant and waste lands and the clearance\n" +
                "of slum areas.\n" +
                "\n" +
                "(iii)  In the interest of national economy the State should have  full\n" +
                "control  over the mineral and oil resources of the country,  including\n" +
                "in  particular, the power to cancel or modify the terms and conditions\n" +
                "of  prospecting licenses, mining leases and similar agreements.   This\n" +
                "is  also  necessary in relation to public utility  undertakings  which\n" +
                "supply  power, light or water to the public under licenses granted  by\n" +
                "the State.\n" +
                "\n" +
                "(iv)  It is often necessary to take over under State management for  a\n" +
                "temporary  period  a  commercial or industrial  undertaking  or   other\n" +
                "property  in  the  public interest or in order to  secure  the   better\n" +
                "management  of  the undertaking or property.  Laws providing for  such\n" +
                "temporary transference to State management should be permissible under\n" +
                "the Constitution.\n" +
                "\n" +
                "(v)  The  reforms  in company law now under   contemplation,  like  the\n" +
                "progressive  elimination of the managing agency system, provision  for\n" +
                "the  compulsory amalgamation of two or more companies in the  national\n" +
                "interest,  the transfer of an undertaking from one company to another,\n" +
                "etc., require to be placed above challenge.\n" +
                "\n" +
                "It is accordingly proposed in clause 3 of the Bill to extend the scope\n" +
                "of  article  31A so as to cover these categories of essential  welfare\n" +
                "legislation.\n" +
                "\n" +
                "4.   As  a corollary to the proposed amendment of article 31A,  it  is\n" +
                "propsed  in  clause 5 of the Bill to include in the Ninth Schedule  to\n" +
                "the  Constitution two more State Acts and four Central Acts which fall\n" +
                "within  the  scope  of sub-clauses (d) and (f) of clause  (1)  of   the\n" +
                "revised article 31A.  The effect will be their complete, retrospective\n" +
                "validation under the provisions of article 31B.\n" +
                "\n" +
                "5.   A  recent judgment of the Supreme Court in Saghir Ahmed  v.   the\n" +
                "State  of U.P.  has raised the question whether an Act providing for a\n" +
                "State  monopoly  in a particular trade or business conflicts with  the\n" +
                "freedom  of trade and commerce guaranteed by article 301, but left the\n" +
                "question  undecided.   Clause  (6) of article 19 was  amended   by  the\n" +
                "Constitution  (First  Amendment)  Act  in order  to  take   such  State\n" +
                "monopolies  out of the purview of sub-clause (g) of clause (1) of that\n" +
                "article,  but no corresponding provision was made in Part XIII of  the\n" +
                "Constitution  with reference to the opening words of article 301.   It\n" +
                "apears from the judgment of the Supreme Court that notwithstanding the\n" +
                "clear  authority of Parliament or of a State Legislature to  introduce\n" +
                "State  monopoly  in a particular sphere of trade or commerce, the  law\n" +
                "might  have to be justified before the courts as being \"in the  public\n" +
                "interest\"  under  article  301  or  as  amounting   to  a   \"reasonable\n" +
                "restriction\"  under  article 304(b).  It is considered that  any   such\n" +
                "question  ought  to be left to the final decision of  the  Legislatue.\n" +
                "Clause  4 of the Bill accordingly proposes an amendment of article 305\n" +
                "to make this clear.\n" +
                "\n" +
                "NEW DELHI;                                           JAWAHARLAL NEHRU.\n" +
                "\n" +
                "The 17th December, 1954.\n" +
                "\n" +
                "\n" +
                "THE CONSTITUTION (EIGHTH AMENDMENT) ACT, 1959\n" +
                "\n" +
                "                                        [5th January,1960.]\n" +
                "\n" +
                "An Act further to amend the Constitution of India.\n" +
                "\n" +
                "BE it enacted by Parliament in the Tenth Year of the Republic of India\n" +
                "as follows:---\n" +
                "\n" +
                "1.   Short  title.-This  Act may be called  the  Constitution  (Eighth\n" +
                "Amendment) Act, 1959.\n" +
                "\n" +
                "2.   Amendment of article 334.-In article 334 of the Constitution, for\n" +
                "the words \"ten years\" the words \"twenty years\" shall be substituted.");
    }
}
